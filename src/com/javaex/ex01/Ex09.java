package com.javaex.ex01;

public class Ex09 {

	public static void main(String[] args) {
		int n1 = 5;
		int n2 = 3;

		System.out.println(n1 < n2);
		System.out.println(n1 > n2);
		
		System.out.println(n1 <= n2);
		System.out.println(3 <= n2);
		
		System.out.println(n1 >= n2);
		System.out.println(3 >= n2);
		
		System.out.println(n1 == n2);
		System.out.println(n1 != n2);
		
		boolean result = n1 != n2;
	}
}
